package com.percussion.pso.importer.manager;

public class SiteDefinition {
	ItemManager manager;
	
	private String navTreeTitle;
	private String path;
	private String navTreeCommunity;
	private boolean navTreeExists;
	
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public boolean isNavTreeExists() {
		return navTreeExists;
	}
	public void setNavTreeExists(boolean navTreeExists) {
		this.navTreeExists = navTreeExists;
	}


	public ItemManager getManager() {
		return manager;
	}

	public void setManager(ItemManager manager) {
		this.manager = manager;
	}

	public void setNavTreeTitle(String navTreeTitle) {
		this.navTreeTitle = navTreeTitle;
	}
	public String getNavTreeTitle() {
		return navTreeTitle;
	}
	public void setNavTreeCommunity(String navTreeCommunity) {
		this.navTreeCommunity = navTreeCommunity;
	}
	public String getNavTreeCommunity() {
		return navTreeCommunity;
	}


}
